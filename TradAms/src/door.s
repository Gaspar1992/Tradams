;;================================================================
;; DOORS_COLLISION_ASM
;;================================================================
doors_collision_asm::
	ld hl , #entity_doors_asm		;; Pointer to Entity Doors
	ld ix , #EXISTING_DOORS			;; Existing Doors
	ld  b , #4						;; Number of Doors
_doors_loop_collision:
	ld  e , (hl)					;; First Part of Pointer
	inc hl							;; Increment HL
	ld  d , (hl)					;; Second part of Pointer
	inc hl							;; Increment HL
	ld a , (ix)						;; A = existing door
	or a 							;; Active Flags
	jp m , _next_doors_iteration
	push ix 						;; Save IX
	push de 						;; Push DE
	pop ix 							;; Pop IX
	ld iy , #player_position		;; Pointer to Player Position
	call collision_entities_asm		;; IF A !=0 THEN COLLISION
	or a 							;; Active Flags
	pop ix 							;; Recover IX
	push ix 						;; Save IX
	call nz , doors_collision_action;; Collision Action
	pop ix 							;; Recover IX
_next_doors_iteration::
	inc ix 							;; Increment IX
	djnz _doors_loop_collision
ret

doors_collision_action:
	ld a , (ix)						;; A = Actual Door
	or a 							;; Type of Door	
	jp nz , prepare_change					
	call z , check_door_changes		
ret

prepare_change:
	push hl 						;; Save HL
	push ix 						;; Save IX
	ld ix , #change_room			;; Change Room Flag
	ld hl ,#CONNECTS_COMPARATOR		;; HL = Pointer to Connects Comparator
	ld a , b 						;; A = B
	dec a 
	push bc 						;; Save BC
	call simple_loop_inc_hl
	pop bc 							;; Recover BC
	ld a, (hl)						;; A = Connect Comp.. Value
	ld (ix) , a 					;; Update Change Room
	pop ix 							;; Recover IX
	pop hl 							;; Recover HL
ret
;;======================================================================================
;;		FUNCTION CHECK DOOR CHANGES
;;======================================================================================
check_door_changes::
	ld iy , #player_bag						;; IX = PLAYER BAG
	ld  a , 1(iy)							;;  A = Number of Player keys
	or  a 									;; Active Flags
	ret z 									;; IF A == 0 THEN RETURN
	inc (ix)								;; Increment Door Value to Open
	push bc 								;; Save BC
	ld a , #1								;; A = Position in Player Bag
	ld e , #-1								;; E = Number of Keys Consumed(NEG)
	call player_add_consumables_values_asm
	ld ix , #actual_room					;; IX = Actual Room
	pop bc 
	push bc 								;; Save BC
	dec b									;; Decrement B to Adapt
	ld c , #1								;; Open Door
	call draw_door_room 					;; Draw Door ROOM
	pop bc 									;; Recover BC
	push hl 								;; Save HL Values
	ld hl , #CONNECTIONS_OPENED				;; HL = Connections Opened Pointer
	push bc 								;; Save BC Values
	dec b 									;; Decrement B to Adapt
	ld a , b 								;; A = B
	call simple_loop_inc_hl					;; Simple HL Loop
	ld a , 1(ix)							;; A = Connection Value
	add (hl)								;; A = A + Door Open Value
	ld 1(ix) , a 							;; IX = New Connection Value
	pop bc 									;; Recover BC
	pop hl 									;; Recover HL
ret
;;===============================================================================
;; FUNCTION DRAW_DOOR_ROOM:
;;
;; Input parameters :  B  = Number of Door (0 = DOWN | 1 = LEFT | 2 = RIGHT | 3 = UP )
;;					   C  = Open Value (0 = Close | 1 = Open) 
;;===============================================================================
draw_door_room::
    ld hl , #DOORS_WIDTH    ;; HL = Pointer to Doors Width
    ld  a , b               ;; A  = Number of Door
    push bc                 ;; Save BC Values
    call simple_loop_inc_hl ;; HL LOOP
    ld e , (hl)             ;; E  = Width Value(HL)
    pop  bc                 ;; Recover BC Values
    push bc                 ;; Save BC Values 
    ld  a , b               ;; A  = Number of Door
    ld hl , #DOORS_HEIGHT   ;; HL = Pointer to Doors Height
    call simple_loop_inc_hl ;; HL LOOP
    ld d , (hl)             ;; D  = height Value(HL)
    pop  bc                 ;; Recover BC Values
    push bc                 ;; Save BC Values 
    push de                 ;; Save DE Values
    call select_door_room   ;; Select Door
    pop  de 				;; Recover DE Value
	pop  bc 				;; Recover BC Value
	call draw_door_tile
ret

;;===============================================================================
;; FUNCTION SELECT_DOOR_ROOM:
;;
;; Input parameters :  B  = Number of Door (0 = DOWN | 1 = UP | 2 = RIGHT | 3 = LEFT )
;; Output parameters: HL  = Pointer to TileSet Door Array 
;;===============================================================================
select_door_room:
    ld hl , #DOORS              ;; Pointer to Doors
    ld a  , b                   ;; A = B
    or a                        ;; Active Flags
    jr z , _give_selected_door
_loop_select_door_room:
    inc hl                      ;; Second Part of Word
    inc hl                      ;; First Part of Word (NEXT DOOR)
    djnz _loop_select_door_room
_give_selected_door:
    ld e , (hl)                 ;; E = First Middle Door Part
    inc hl                      ;; Next Position Array TileSet
    ld d , (hl)                 ;; D = Second Middle Door Part
	ex de , hl 					;; Exchange DE <-> HL                 
ret
;;===============================================================================
;; FUNCTION DRAW_DOOR_TILE:
;;
;; Input parameters :  C  = Open Value (0 = Close | 1 = Open)
;;					   B  = Number of Door				   
;;                     DE = SIZE
;;					   HL = TILE  
;;===============================================================================
draw_door_tile:

	push de 						;; Save DE Values (SIZE)
	push hl 						;; Save Pointer to Array Tile
	push bc 						;; Save BC Values

	ld a , b 						;; A = Number of Door(B)
	add b 							;; A = Number of Door(B)*2
	ld hl , #DOORS_POSITIONS		;; HL = Doors Positions
	call simple_loop_inc_hl 		;; HL LOOP
	;; GET POSITION YX INTO HL

	ld c , (hl)						;; B = Y Value (HL)
	inc hl 							;; Next Pointer Position  (X Value)
	ld b , (hl)						;; C = X Value

	ld de , #Screen_Start			;; Pointer to Screen
	call cpct_getScreenPtr_asm		;; Get Screen Pointer
	ex    de, hl                    ;; DE = pointer to video memory location to draw the sprite

	pop bc 							;; Recover BC Values
	pop hl 							;; Recover Pointer to Array Tile

	ld a , c 						;; A = State Door(C)
	cp #DOOR_OPEN					;; COMPARE
	jr nz ,_closed_door_draw

_opened_door_draw:
	ld b , #TILE_DOOR_OPEN			;; B = Tile Door Open
	ld a , b 						;; A = Tile Door Open(B)
	add b 							;; A = Tile Door Open(A)*2

	call simple_loop_inc_hl		
_closed_door_draw:		
	ld c , (hl)						;; GET FIRST PART VALUE
	inc hl 							;; GET NEXT PART VALUE
	ld b , (hl)						;; GET SECOND PART VALUE
	ld h , b 						;; H = First Part of Pointer
	ld l , c						;; L = Second Part of Pointer

_draw_tile:
	pop bc 							;; Recover Size
	call cpct_drawSprite_asm		;; Draw Sprite
ret


